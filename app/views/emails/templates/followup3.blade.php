Cara Mengatasi bau kaki


Pakailah sepatu yang bisa memberikan sirkulasi udara agar didalam tidak lembab dan tidak menimbulkan keringat pada kaki.Jangan mempergunakan bahan kaos kaki yang terbuat dari nilon,dimana bahan ini terasa panas dan tidak menyerap keringat.

Lumuri daleman kaos kaki dengan tawas bubuk,jika tidak ada boleh menggunakan bubuk kopi murni sebelum dipakai pada kaki.

Jika anda bekerja dikantor,manfaatjkan waktu istirahat untuk sejenak melepaskan sepatu.Cuci kaki anda menggunakan sabun dengan air hangat,dan kemudian keringkan.Ritual mencuci kaki ini juga lakukan dirumah selepas dari sepatu.

Karena anda bermasalah dengan bau kaki,maka jangan hanya mengandalkan satu sepatu untuk seminggu penuh.Gonta gantilah sepatu anda sehari sekali sementara yang lainnya dijemur untuk mematikan jamur dan bakteri.

Jika tips diatas tidak memberikan hasil  dan kaki anda masih berkeringat secara berlebih dengan bau yang luar biasa,hubungilah ahli kesehatan atau dokter.Karena hal itu tidak lazim,  bisa jadi diakibatkan oleh masalah kesehatan lain yang lebih serius.

Semoga dengan setelah menjalankan tips menghilangkan bau kaki diatas, anda akan mendapatkan rasa percaya diri kembali jika melepas sepatu didepan publik.
