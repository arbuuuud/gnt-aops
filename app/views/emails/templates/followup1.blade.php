Buah mengkudu merupakan salah satu buah asli yang berasal dari Asia tenggara. Bentuk buahnya yang agak lonjong dan agak berbintik pada permukaan, namun banyak orang yang kurang menyukai aroma buah ini. Pada umumnya buah mengkudu dimanfaatkan oleh sebagian masyarakat untuk bahan makanan seperti sayur, rujak dan juga jus. Bagi sebagian orang yang tidak menyukai buah mengkudu ini, ada baiknya untuk mengenal lebih dekat buah mengkudu ini, berikut sejumlah manfaat buah mengkudu yang selama ini belum diketahui.

Di dunia ini ada 80 macam mengkudu, namun yang bisa masuk atau dikonsumsi oleh manusia hanya sebanyak 20 jenis saja. Sedangkan 60 jenis lainnya adalah racun dan jika dikonsumsi akan membahayakan tubuh. Buah pace bermanfaat untuk kesehatan karena kandungannya yang lengkap.

Kandungan Zat Buah Mengkudu

manfaat-buah-mengkuduPada Penelitian NCCAM mencakup studi tentang buah mengkudu untuk kanker, menentukan keamanan dan potensi dampak pada tumor dan gejala, serta studi laboratorium efek mengkudu pada sel-sel kanker prostat.

Dalam beberapa penelitian laboratorium, buah mengkudu menunjukkan kandungan antioksidan, merangsang kekebalan tubuh dan bersifat melawan tumor. Hasil tersebut telah menunjukkan bahwa buah mengkudu, menjamin penelitian lebih lanjut untuk kondisi seperti kanker dan penyakit kardiovaskular.  Berikut ini berbagai macam zat yang ada dalam buah pace atau mengkudu :

Kandungan zat buah mengkudu sangat baik bagi tubuh, beberapa diantaranya adalah sebagai berikut :

Zat Terpenoid – Kandungan zat terpenoid pada mengkudu membantu proses sintesis organic yang berguna untuk pemulihan sel-sel tubuh.
Kandungan Nutrisi – Protein, vitamin dan mineral yang penting dengan lengkap didapat dari buah mengkudu. Antioksidan bernama selenium yang terdapat pada buah mengkudu merupakan salah satu jenis antioksidan yang hebat.
Zat anti bakteri ( misalnya, Pseudomonas aeruginosa, Staphylococcus aureus, Protens morganii, Escherichia coli dan Bacillus subtilis) – Zat tersebut membunuh bakteri penyebab infeksi. Manfaat buah mengkudu juga berguna mengontrol bakteri mematikan seperti Pathogen. Hal ini membuat buah pace bisa membunuh bakteri yang bisa menyebabkan infeksi. Berikut ini berbagai macam bakteri yang bisa dibunuh menggunakan buah pace :
Pseudomonas aeruginosa.
E coli.
Bacillus subtilis.
Protens morganii.
Staphylococcus aureus.
Salmonella.
S. typhi.
Scotmuelleri.
Shigella dusenteriae.
S. pradysenteriae.
Zat anti kanker – Kandungan zat-zat anti kanker di dalam buah mengkudu sangat efektif untuk melawan sel-sel abnormal, yang menjadikan timbulnya masalah gejala kanker.
Nutrisi. Pace memiliki banyak nutrisi di dalamnya. Secara umum, pace adalah buah yang memberikan nutrisi secara lengkap. Berikut ini berbagai macam nutrisi yang ada dalam buah mengkudu :
Protein.
Vitamin.
Mineral cukup. Mineral ada di dalam buah dan juga daun pace.
Selenium. Selenium merupakan mineral yang berfungsi sebagai antioksidan tinggi.
Senyawa. Di dalam mengkudu juga terdapat berbagai macam senyawa aktif. Senyawa dalam buah mengkudu adalah senyawa plants sterois, alizarin, lycine, caprylic acid, sosium, proxeronine, antra quinines, trace elemens, phenylalanine, magnesium.
Scolopetin. Scolopetin adalah sejenis senyawa yang bermnafaat untuk mengusir peradangan dan juga alergi.
Xeronine dan Proxeronine.
Zat bioaktif.
Zat iridoid.
Serat.
Metil.
Asetil.
Morindone.
Asam kapril.
Ciri-Ciri Tanaman Mengkudu

manfaat buah mengkuduAgar tidak tertipu dengan tanaman atau tumbuhan lain sebaiknya kita mengetahui ciri-ciri dari tanaman mengkudu. Berikut ini adalah ciri-ciri dari tanaman mengkudu atau pace :

Pohon. Pohon mengkudu tidak terlalu besar dan juga tidak terlalu tinggi. Tingginya hanyalah 4-6 meter saja. Dahan dari pohon mengkudu kaku, akarnya tunggang dan memiliki dahan kasar. Warna batangnya cokelat dan keabu-abuan.
Daun. Daun dari pohon mengkudu berdaun tebal. Daun mengkudu letaknya berhadap-hadapan. Daunnya lebar-lebar dan besar-besar. Tepi daunnya rata.
Bunga. Bunga dari tanaman mengkudu majemuk, bunganya bulat. Tangkai bunga mengkudu sepanjang 4 cm. Bunga mengkudu diberi nama buah banci, bermahkota bunga putih, bentuknya menyerupai corong dan memiliki panjang 1,5 cm. Ada benang sari dan menancap di mulut mahkota. Putik bunga mengkudu tersebut berjumlah dua.
