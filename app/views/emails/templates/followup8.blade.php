Kapalan atau callus atau yang biasa kitah ketahui sebagai penebalan pada kulit yang yang diakibatkan oleh terlalu seringnya bagian kulit tersebut bergesekan dengan benda keras.Hal ini adala prose alami dari kulit untuk melakukan penyesuaian terhadap keadaan yang menerpanya. Umumnya penebalan kulit ini terdapat di bagian tumit, sisi atau telapak karena bagian kulit itu setiap hari bergesekan dengan sepatu/sandal sempit.
Kapalan juga bisa disebabkan karena sering terkena deterjen yang terlalu keras sehingga membuat kulit menjadi kering.
Kalau hal ini terjadi pada tangan,ini bisa menyebabkan pobhia bersalaman :D :D :D


Berikut Cara Alami Menghilangkan Kapalan :

1. Rendam kaki dalam air hangat selama kurang lebih setangah jam, kemudian gosok kulit kaki dengan sikat lembut atau menggunakan batu apung untuk mengangkat sel kulit mati. Lakukan hal ini 2-3 kali dalam seminggu.

2. Gunakan pelembab/lotion untuk kaki/tangan agar kulit kaki atau tangan tetap terjaga kelembabannya.

3. Untuk melembabkan bisa juga menggunakan minyak zaitun yang kaya akan vitamin E sebagai pelembab yang baik untuk mengurangi kulit kaki yang kering.

4. Beberapa bahan alami ini bisa digunakan untuk mengatasi kapalan :

Oleskan getah daun kemboja pada bagian yang kapalan, lakukan setiap pagi dan sore hingga kapalan menipis dan menghilang.
Lemon, perasan jeruk lemon segar bisa mengatasi rasa nyeri yang biasanya muncul pada kapalan. Caranya, bersihkan area yang mengalami kapalan, lalu oleskan area yang kapalan dengan perasan jeruk lemon, dan diamkan selama satu malam.
Pepaya mentah, caranya haluskan beberapa potong pepaya mentah lalu tempelkan pada area yang kapalan, 2-3 kali sehari.
Bawang Putih, haluskan bawang putih kemudian tempelkan pada kapalan, diamkan selama satu malam.
Kentang, parutan kentang tempelkan di bagian yang kapalan dan tutup dengan kain kassa, lalu diamkan selama 2-3 jam.

